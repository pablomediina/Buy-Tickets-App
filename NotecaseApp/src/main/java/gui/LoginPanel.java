    private void userTxtMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_userTxtMousePressed
        if (userTxt.getText().equals("Ingrese su nombre de usuario")) {
            userTxt.setText("");
            userTxt.setForeground(Color.black);
        }
        if (String.valueOf(passTxt.getPassword()).isEmpty()) {
            passTxt.setText("********");
            passTxt.setForeground(Color.gray);
        }
    }//GEN-LAST:event_userTxtMousePressed

    private void passTxtMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_passTxtMousePressed
        if (String.valueOf(passTxt.getPassword()).equals("********")) {
            passTxt.setText("");
            passTxt.setForeground(Color.black);
        }
        if (userTxt.getText().isEmpty()) {
            userTxt.setText("Ingrese su nombre de usuario");
            userTxt.setForeground(Color.gray);
        }
    }//GEN-LAST:event_passTxtMousePressed

    private void loginBtnTxtMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginBtnTxtMouseClicked
        if (userTxt.getText().isEmpty() || userTxt.getText().equals("Ingrese su nombre de usuario") || passTxt.getText().isEmpty() || String.valueOf(passTxt.getPassword()).equals("********")) {
            JOptionPane.showMessageDialog(this, "Ingrese todos los datos", "Error", JOptionPane.ERROR_MESSAGE);
            
        } else  {
            JOptionPane.showMessageDialog(this, "Bienvenido:\nUsuario: " + userTxt.getText() + "\n", "LOGIN", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            DashboardPanel abrir = new DashboardPanel(controller);
            abrir.setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_loginBtnTxtMouseClicked

